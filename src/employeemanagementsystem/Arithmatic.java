public class Arithmatic 
{
    public int sum(int a, int b)
    {
        int c = a + b;
        return c;
    }
    
    public int sum(int a, int b, int c)
    {
        int d = a + b + c;
        return d;
    }
    
    public float sum(float a, int b)
    {
        float c = a + b;
        return c;
    }
    
    public float sum(int a, float b)
    {
        float c = a + b;
        return c;
    }
}
